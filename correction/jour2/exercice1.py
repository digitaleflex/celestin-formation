# Déclaration des variables
prenom = "Alice"  # Chaîne de caractères
age = 25          # Entier
poids = 68.5      # Flottant


# Affichage des types
print(f"Type de prenom: {type(prenom)}")  # <class 'str'>
print(f"Type de age: {type(age)}")        # <class 'int'>
print(f"Type de poids: {type(poids)}")    # <class 'float'>