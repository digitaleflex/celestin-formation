# Déclaration des nombres
entier = 10
flottant = 3.5

# Opérations
addition = entier + flottant
soustraction = entier - flottant
multiplication = entier * flottant
division = entier / flottant
modulo = entier % int(flottant)  # Modulo nécessite un entier
puissance = entier ** flottant

# Affichage des résultats
print(f"Addition: {addition}")
print(f"Soustraction: {soustraction}")
print(f"Multiplication: {multiplication}")
print(f"Division: {division}")
print(f"Modulo: {modulo}")
print(f"Puissance: {puissance}")