# Déclaration de la phrase
phrase = "Python est un langage puissant ! et je l'aime beaucoup."

# Opérations sur la chaîne
longueur = len(phrase)
minuscules = phrase.lower()
majuscules = phrase.upper()
remplacement = phrase.replace("puissant", "incroyable")
premiers_caracteres = phrase[:6]

# Affichage des résultats
print(f"Longueur: {longueur}")
print(f"Minuscules: {minuscules}")
print(f"Majuscules: {majuscules}")
print(f"Remplacement: {remplacement}")
print(f"6 premiers caractères: {premiers_caracteres}")